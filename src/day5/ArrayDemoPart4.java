package day5;

public class ArrayDemoPart4 {

	public static void main(String[] args) {

		// there is no need to declare an array size
		String[] names = { "Mukesh", "Anuska", "Sumit", "Sachin", "Uma", "Bhairu" };

		// Using forEach
		for (String name : names) {
			System.out.println(name);
		}
	}

}
